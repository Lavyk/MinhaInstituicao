    private void btnRemoverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoverMouseClicked
        String matricula = null;
        try{
                matricula = userData.get("matricula");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null,"Você não selecionou nenhum usuário!");
        }
        
        if(matricula == null){
    
        } else {
            try {
                user.deletarUser(matricula);
                dispose();
                Usuarios main = new Usuarios();
                main.setVisible(true);
                
            } catch (SQLException ex) {
                Logger.getLogger(RemoverUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
            //dispose();
  
    }//GEN-LAST:event_btnRemoverMouseClicked

