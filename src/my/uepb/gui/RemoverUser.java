    public void mudarTextos(Map userMapa){
        userData = userMapa;
        textNome.setText(userData.get("nome"));
        textMatricula.setText(userData.get("matricula"));
        textSexo.setText(userData.get("sexo"));
        
        String cargo = "";
        
        if(userData.get("cargo") == null){
            
        } else {
            if(userData.get("cargo").equals("1")){
                cargo = "Aluno";
            }
            if(userData.get("cargo").equals("2")){
                cargo = "Professor";
            }

            if(userData.get("cargo").equals("3")){
                cargo = "Coordenador";
            }
            textCargo.setText(cargo);
        }
    }
