    private void textPesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textPesquisaKeyPressed
        String nome = textPesquisa.getText();
        if(nome.equals("Pesquisar matricula")){
            textPesquisa.setText("");
            textPesquisa.setForeground(Color.BLACK);
        }
        String pesquisa = textPesquisa.getText();
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            
            try {
                mudarTextos(user.pesquisarUser(pesquisa));
            } catch (SQLException ex) {
                Logger.getLogger(RemoverUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_textPesquisaKeyPressed

    private void textPesquisaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textPesquisaKeyTyped
        String caracteres="0987654321";
        if(!caracteres.contains(evt.getKeyChar()+"")){
            evt.consume();        
        }
        
        String text = textPesquisa.getText();

        if(text.length() == 9){
            evt.consume();
        }
        
       
        
        
    }//GEN-LAST:event_textPesquisaKeyTyped

